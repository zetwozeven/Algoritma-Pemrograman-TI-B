# Operasi matematika sederhana
a = 10
b = 5

print("\nOperasi Matematika:")
print(a, "+", b, "=", a + b)   # Penjumlahan
print(a, "-", b, "=", a - b)   # Pengurangan
print(a, "*", b, "=", a * b)   # Perkalian
print(a, "/", b, "=", a / b)   # Pembagian (hasil float)
print(a, "//", b, "=", a // b) # Pembagian bulat
print(a, "%", b, "=", a % b)   # Sisa bagi

# Menghitung sesuatu yang lebih besar
x = 1000
y = 250
print("\nContoh perhitungan besar:")
print(f"{x} + {y} = {x + y}")
print(f"{x} - {y} = {x - y}")
print(f"{x} * {y} = {x * y}")

