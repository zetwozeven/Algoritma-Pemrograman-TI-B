#soal 3 (if Else)
# Program cek kelulusan berdasarkan nilai ujian

# Menyimpan nilai ujian ke dalam variabel
nilai = 75  # Ganti angka ini sesuai nilai ujian

# Menampilkan nilai ujian
print("Nilai ujian =", nilai)

# Mengecek kelulusan menggunakan if else
if nilai >= 60:
    print("Status: Lulus")          # Jika nilai >= 60, tampilkan "Lulus"
else:
    print("Status: Tidak Lulus")    # Jika nilai < 60, tampilkan "Tidak Lulus"

# Menampilkan pesan penutup
print("Terima kasih telah menggunakan program cek kelulusan!")
