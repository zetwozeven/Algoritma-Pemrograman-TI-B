# Program operasi matematika dasar
#program ini menyimpan dua bilangan dan menghitung penjumlahan dan perkalian

# Menyimpan dua bilangan
a = 5                   #variabel 'a' menyimpan angka 5
b = 10                  #variabel 'b' menyimpan angka 10

# Menampilkan bilangan yang disimpan
print("Bilangan a =", a)
print("Bilangan b =", b)

# Menghitung penjumlahan
jumlah = a + b                           #menjumlahkan 'a' dan 'b' ,hasil disimpan di variabel 'jumlah'
print("Hasil penjumlahan =", jumlah)     #menampilkan hasil penjumlahan 

# Menghitung perkalian
kali = a * b                             #mengalikkan 'a' dan 'b' , hasil di simmpan di variabel 'kali' 
print("Hasil perkalian =", kali)         #menamapilkan hasil perkalian 

# Menampilkan hasil akhir lagi
print("Penjumlahan =", jumlah)            #menampilkan penjumlahan sekali lagi 
print("Perkalian =", kali)                #menampilkan perkalian sekali lagi
