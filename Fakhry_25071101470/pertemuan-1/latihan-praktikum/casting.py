# casting adalah mengonversikan tipe data awal menjadi tipe data yang berbeda

# contohnya

x = "10" # variabel x bertipe data string
y = int(x) # variabel y adalah konversi variabel x yang bertipe data string menjadi tipe data number

print(y + 5) # maka hasil dari y + 5 adalah 15 karena variabel y bernilai 10 dengan tipe data number setelah dikonversi

