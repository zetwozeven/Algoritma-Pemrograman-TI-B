# if else adalah fungsi pemilihan jika dan maka

#contohnya

nilai = 80 #variabel nilai bernilai 80

if nilai >= 75: # jika nilai lebih dari atau sama dengan 75 akan menjalankan perintah print("lulus")
    print("Lulus")
else: # jika tidak maka akan menjalankan perintah print("tidak lulus")
    print("Tidak lulus")

# ada penulisan lain selain yang diatas yaitu
print ('lulus') if nilai >= 75 else print('tidak lulus')