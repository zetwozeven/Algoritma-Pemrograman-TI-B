# string atau yang disebut text adalah tipe data yang berupa karakter

#pada contoh ini variabel berisi python
text = 'Python'

# ada beberapa fungsi yang bisa dilakukan dalam tipe data text

#fungsi pertama ada upper dan lower, gunanya untuk mengubah karakter dari kecil menjadi besar dan besar menjadi kecil
print(text.upper())
print (text.lower())

#fungsi kedua ada penyisihan karakter menggunakan array
print(text[0])
print(text[0:2])

# fungsi ketiga ada penyambungan karakter dan pengitung panjang karakter
a = 'pyt'
b = 'hon'
c = a + b
print(c)

