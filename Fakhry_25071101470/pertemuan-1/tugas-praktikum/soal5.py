def penjumlahan(a, b):
    return a + b

def pengurangan(a, b):
    return a - b

bilangan1 = int(input('masukkan bilangan pertama: '))
bilangan2 = int(input('masukkan bilangan kedua: '))

print('Penjumlahan =', penjumlahan(bilangan1, bilangan2))
print('Pengurangan =', pengurangan(bilangan1, bilangan2))