x = 15
y = 4

#operator aritmatika
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

#operator perbandingan
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

#logika operator (and, or, not)
x = 5
print(x < 5 or x > 10)
print(x > 0 and x < 10)
print(not(x > 3 and x < 10))

