'''
int (Bilangan bulat)
float (Bilangan desimal)
complex (Kombinasi bilangan real dan imajiner yang dilambangkan dengan huruf j)

a = 9    int
b = 1.5  float
c = 5j   complea

Untuk menentukan sebuah tipe data, perlu digunakan syntax print(type(...))
'''

#INTEGER

a = 7
b = 123456789
c = -56789

print(type(a))
print(type(b))
print(type(c))

#FLOAT

a = 1.25
b = 2.50
c = -8.75

print(type(a))
print(type(b))
print(type(c))

#COMPLEX

a = 9+7j
b = 8j
c = -5j

print(type(a))
print(type(b))
print(type(c))