string = '1' #Tipe data awal variabel string adalah string
integer = 8 #Tipe data awal variabel integer adalah int
floats = 3.5 #Tipe data awal variabel floats adalah float

string_to_int = int(string) #Mengubah nama variabel string menjadi string_to_int dan tipe datanya menjadi int
string_to_float = float(string) #Mengubah nama variabel string menjadi string_to_float dan tipe datanya menjadi float
int_to_string = str(integer) #Mengubah nama variabel string menjadi int_to_string dan tipe datanya menjadi string
int_to_float = float(integer) #Mengubah nama variabel string menjadi int_to_float dan tipe datanya menjadi float
float_to_int = int(floats) #Mengubah nama variabel floats menjadi float_to_int dan tipe datanya menjadi int
float_to_string = str(floats) #Mengubah nama variabel floats menjadi float_to_int dan tipe datanya menjadi string

print(type(string)) #Untuk mengecek tipe data variabel string
print(type(string_to_int)) #Untuk mengecek tipe data variabel string_to_int
print(type(string_to_float)) #Untuk mengecek tipe data variabel string_to_float
print(type(integer)) #Untuk mengecek tipe data variabel integer
print(type(int_to_string)) #Untuk mengecek tipe data variabel int_to_string
print(type(int_to_float)) #Untuk mengecek tipe data variabel int_to_float
print(type(floats)) #Untuk mengecek tipe data variabel floats
print(type(float_to_int)) #Untuk mengecek tipe data variabel float_to_int
print(type(float_to_string)) #Untuk mengecek tipe data variabel float_to_string