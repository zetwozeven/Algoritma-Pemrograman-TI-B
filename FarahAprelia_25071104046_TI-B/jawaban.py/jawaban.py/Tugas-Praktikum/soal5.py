#Fungsi

def hitung(a, b): #Definisi fungsi bernama hitung dengan dua parameter, yaitu a dan b
  return a + b #Mengembalikan hasil penjumlahan a + b
hasil1 = hitung(55, 45) #Pemanggilan fungsi serta memasukkan value ke dalam variabel hasil1

def hitung(a, b): #Definisi fungsi bernama hitung dengan dua parameter, yaitu a dan b
  return a - b #Mengembalikan hasil pengurangan a - b
hasil2 = hitung(55, 45) #Pemanggilan fungsi serta memasukkan value ke dalam variabel hasil2

print("Penjumlahan:", hasil1) #Mencetak value dari variabel hasil1
print("Pengurangan:", hasil2) #Mencetak value dari variabel hasil2