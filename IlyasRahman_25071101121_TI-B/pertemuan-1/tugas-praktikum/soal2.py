'''
Buat sebuah program Python yang:
1. Menyimpan dua bilangan ke dalam dua variabel
2. Menghitung hasil penjumlahan dan perkalian dari kedua bilangan tersebut
3. Menampilkan hasilnya ke layar
Contoh keluaran:
Hasil penjumlahan = 15
Hasil perkalian = 50
'''
# Menyimpan dua bilangan ke dalam dua variabel
bilangan1 = 10
bilangan2 = 5

# Menghitung hasil penjumlahan dan perkalian dari kedua bilangan tersebut
hasil_penjumlahan = bilangan1 + bilangan2
hasil_perkalian = bilangan1 * bilangan2

# Menampilkan hasilnya ke layar
print("Hasil penjumlahan =", hasil_penjumlahan)
print("Hasil perkalian =", hasil_perkalian)