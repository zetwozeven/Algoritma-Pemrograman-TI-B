'''
Buat sebuah program Python yang:
1. Menyimpan sebuah nilai ujian (0–100) ke dalam variabel
2. Jika nilai lebih besar atau sama dengan 60, tampilkan:
Lulus
Jika nilai kurang dari 60, tampilkan:
Tidak Lulus
'''
# Menyimpan sebuah nilai ujian (0–100) ke dalam variabel
nilai_ujian = 75

# Memeriksa apakah nilai ujian lulus atau tidak
if nilai_ujian >= 60:
    print("Lulus")
else:
    print("Tidak Lulus")