#int
x = int(5)   #nilai variabel x yang awalnya bertipe data int di ubah menjadi int, jadi = 5 
y = int(7.8) #nilai variabel y yang awalnya bertipe data float di ubah menjadi int, jadi = 7 
z = int("10") #nilai variabel z yang awalnya bertipe data string di ubah menjadi int, jadi = 10 
a = int('5')   #nilai variabel a yang awalnya bertipe data string di ubah menjadi int, jadi = 5 
b = int(8.4) #nilai variabel b yang awalnya bertipe data float di ubah menjadi int, jadi = 8 
c = int("16") #nilai variabel c yang awalnya bertipe data string di ubah menjadi int, jadi = 16 

print(x)
print(y)
print(z)
print(a)
print(b)
print(c)

#float
x = float(5) #nilai variabel x yang awalnya bertipe data int di ubah menjadi float, jadi = 5.0 
y = float(10.8) #nilai variabel y yang awalnya bertipe data float di ubah menjadi float, juga jadi = 10.8 
z = float("7") #nilai variabel z yang awalnya bertipe data string di ubah menjadi float, jadi = 7.0 
w = float("2.9") #nilai variabel w yang awalnya bertipe data string di ubah menjadi float, jadi = 2.9 

print(x)
print(y)
print(z)
print(w)

#string
x = str("s5") #nilai variabel x yang awalnya bertipe data string di ubah menjadi string juga, jadi = "s5"
y = str(7) #nilai variabel y yang awalnya bertipe data int di ubah menjadi string, jadi = "7"
z = str(4.0) #nilai variabel z yang awalnya bertipe data float di ubah menjadi string, jadi = "4.0"

print(x)
