# Jenis-jenis numbers
print("-Jenis-jenis numbers")
x = 1 # int
y = 2.5 # float
z = 2j # complex
print(" ")

## int adalah tipe data untuk angka yang berbentuk bilangan bulat
print("Int:")
x = 100
y = -50000000
z = 2
print(x)
print(y)
print(z)
print(" ")

## float adalah tipe data untuk angka yang berbentuk desimal
print("floatL:")
x = 3.14
y = -24.7
z = 6.7
print(x)
print(y)
print(z)
print(" ")

## complex adalah tipe data untuk angka yang bisa menyimpan banyak nilai dalam satu variabel, ditulis dengan 'j'
print("Complex:")
x = 4+2j
y = 8j
z = -12j
print(x)
print(y)
print(z)
print(" ")

## Konversi tipe data
print("-Konversi tipe data-")
x = float(1) # Konversi ke float
y = int(3.5) # Konversi ke int
z = complex(2) # Konversi ke complex
print(x)
print(y)
print(z)
print(type(x))
print(type(y))
print(type(z))
