## ada 3 tipe bilangan

# 1. Integer / Bilangan Bulat
# bisa berupa bilangan positif, nol,  dan negatif asal tidak berkoma
x = 102052754
y = 0
z = -191919

print(x)  #output: 102052754
print(y)  #output: 0
print(z)  #output: -191919


print(type(x))  #output: int
print(type(y))  #output: int
print(type(z))  #output: int



# 2. Float / Bilangan Rill
# bilangan berkoma
x = 3.14
y = 12E4  #bisa juga menggunakn e untuk kelipatas 10
z = 12E-2 # kalau minus dibagi 10

print(x)  #output: 3.14
print(y)  #output: 120000.0
print(z)  #output: 0.0012

print(type(x))  #output: float
print(type(y))  #output: float
print(type(z))  #output: float

# 3. Complex
# angka yang ditulis dengan huruf (j) sebagai bagian dari bilangan imaginary
x = 3+5j
y = 5j
z = -5j

print(x)  #output: (3+5j)
print(y)  #output: 5j
print(z)  #output: (-0-5j)

print(type(x))  #output: complex
print(type(y))  #output: complex
print(type(z))  #output: complex



