## kita dapat menggunakan fungsi print() untuk menghasilkan keluaran atau menampilkannya ke layar
print('ohayou sekai')

# kita dapat memilih menggunakan petik satu atau petik dua untuk teks di python karena tetap menghasilkan output yang sama
print("halo") # output : halo
print('halo') # output : halo

# kita juga bisa menyambungkan dua output print menjadi satu baris
print("Halo semuanya", end=", ")
print("kabarnya pada baik\" kan?.")

## Kita juga dapat menggunakan fungsi print() untuk menampilkan number(angka) ke layar
print(2)
print(58)

# Kita juga bisa melakukan operasi matematika didalamnya
print(2 + 1)
print(5 * 5)

# Dan kita juga bisa menggabungkan text dan number dalam satu output/keluaran
print("ayo", 2 , "oke gas oke gas")
