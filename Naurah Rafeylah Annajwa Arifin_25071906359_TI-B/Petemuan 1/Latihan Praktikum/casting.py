string = '1' #sebuah variabel string (str) dengan value '1'

string_to_numbers = int(string) #untuk mengubah tipe 'string' menjadi tipe int

print(type(string)) #menampilkan tipe data string
print(type(string_to_numbers)) #menampilkan tipe data str yang sudah diubah menjadi int

