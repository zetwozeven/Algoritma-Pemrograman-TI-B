#perhitungan penjumlahan dan perkalian kedua bilangan
penjumlahan = x + y
perkalian = x * y

#output
print('Hasil jumlah dari 6 dan 12 adalah', penjumlahan) #output = Hasil jumlah dari 6 dan 12 adalah 18
print('Hasil kali dari 6 dan 12 adalah', perkalian) #output = Hasil kali dari 6 dan 12 adalah 72