'''
Soal 3 (If Else)

Buat sebuah program Python yang:
1. Menyimpan sebuah nilai ujian (0-100) ke dalam variabel
2. Jika nilai lebih besar atau sama dengan 60, tampilkan:
Lulus
Jika nilai kurang dari 60, tampilkan:
Tidak Lulus
'''

#menyimpan nilai ujian, yaitu 90
nilai = 90

if nilai >= 60:
    print('Lulus') #jika nilai lebih besar atau sama dengan 60, maka outputnya 'Lulus'
else:
    print('Tidak Lulus') #tetapi jika nilai lebih kecil dari 60, maka outputnya 'Tidak Lulus'