#Bagian ini menjelaskan loop menggunakan perintah while
#While ini maksudnya akan terjadi perulangan selama kondisinya benar

i = 1
while i < 6:
    print(i)
    if i == 3:
        break  #Break akan menghentikan loop jika i sudah berulang sampe 3
    i += 1

#Ingat untuk menghentikan loop, jangan sampai tidak terbatas