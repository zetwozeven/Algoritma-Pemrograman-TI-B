#Bagian ini membahas tentang operator aritmatika, perbandingan, dan juga logika

#Operasi aritmatika
x = 15
y = 4

print(x + y)    #Penjumlahan
print(x - y)    #Pengurangan
print(x * y)    #Perkalian
print(x / y)    #Pembagian
print(x % y)    #Modulus (Sisa bagi)
print(x ** y)   #Eksponen
print(x // y)   #Pembagian lantai (pembulatan kebawah)

#Operasi perbandingan
print(x == y)   #Sama dengan
print(x != y)   #Tidak sama dengan

#Operasi logika

x = 5

print(x > 0 and x < 10) #True jika kedua kondisi benar

print(x < 5 or x > 10)  #True jika salah satu kondisi benar


