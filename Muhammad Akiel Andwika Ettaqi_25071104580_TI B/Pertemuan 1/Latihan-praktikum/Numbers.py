#Bagian ini berisi tipe data angka di python

x = 5   #integer (berisi bilangan bulat)
y = 2.8 #float   (berisi bilangan desimal)
z = 1j  #complex (berisi bilangan riil dan juga imajiner)

print(type(x))
print(type(y))
print(type(z))

#Kita juga bisa mengkonversi tipe data ini ke tipe data lain

a = float(x)
b = int(y)

print(a) #hasilnya jadi desimal (5.0)
print(b) #hasilnya jadi bilangan bulat (2)