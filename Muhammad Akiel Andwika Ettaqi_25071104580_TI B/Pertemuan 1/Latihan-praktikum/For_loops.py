#Bagian ini berisi pengulangan dengan menggunakan perintah for
#Bedanya dengan while loop adalah untuk for ini kita sudah tau berapa kali perulagan akan dilakukan

buah = ['apel', 'mangga', 'jeruk']

for x in buah: #kita melakukan loop yang ada didalam list buah
    print(x)

for x in range(5): #kita melakukan loop yang batasnya adalah 4 (karena indeks dimulai dari 0)
    print(x)